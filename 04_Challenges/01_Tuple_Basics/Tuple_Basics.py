# Creating a tuple with different types of values
my_tuple = (42, 3.14, "Python", True, (1, 2, 3))

# Printing the whole tuple
print(my_tuple)

# Printing the third element (index 2)
print(my_tuple[2])

# Getting the first element from the nested tuple
print(my_tuple[4][0])
